# slicing operation
# syntax a[start index:end index:step value]

a="ram is boy"
print(a[0:6:1])

a="ram is boy"
print(a[0:8:2])

a="ram is boy"
print(a[:6:3])

a="ram is boy"
print(a[-1:-6:-1])

a="ram is boy"
print(a[-1:-6:-4])

s="abcdefghijkl"
print(s[8:-1:-2])

s="abcdefghijkl"
print(s[8:-1:2])